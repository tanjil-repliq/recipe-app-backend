"""
Calculator functions
"""


def add(x, y):
    """Add x and y and return the result"""
    return x + y


def subtract(x, y):
    """Subtract x and y and return the result"""
    return y - x


def multiply(x, y):
    """Multiply x and y and return the result"""
    return x * y


def divide(x, y):
    """Divide x and y and return the result"""
    return x / y
